import streamlit as st

import re
import pickle

from tensorflow.keras.models import load_model
from tensorflow.keras.preprocessing.sequence import pad_sequences

st.title("AI MBTI Personality Chatbot")

IE_model = load_model("src/IE_model.h5")

NS_model = load_model("src/NS_model.h5")

TF_model = load_model("src/TF_model.h5")

JP_model = load_model("src/JP_model.h5")

with open('src/tokenizer.pkl', 'rb') as f:
    tokenizer = pickle.load(f)

max_len = 200

def clean_text(text):

    text = text.lower()

    text = re.sub(r'http\S+', '', text)

    text = re.sub(r'[^a-zA-Z\s]', ' ', text)

    text = re.sub(r'\s+', ' ', text).strip()

    return text

def predict_mbti(text):

    # clean text
    text = clean_text(text)

    # convert text to sequence
    seq = tokenizer.texts_to_sequences([text])

    # pad sequence
    padded = pad_sequences(
        seq,
        maxlen=max_len,
        padding='post',
        truncating='post'
    )

    # predictions
    IE_pred = IE_model.predict(padded)[0][0]

    NS_pred = NS_model.predict(padded)[0][0]

    TF_pred = TF_model.predict(padded)[0][0]

    JP_pred = JP_model.predict(padded)[0][0]

    # probabilities to letters
    ie = 'E' if IE_pred >= 0.1 else 'I'

    ns = 'S' if NS_pred >= 0.1 else 'N'

    tf = 'F' if TF_pred >= 0.1 else 'T'

    jp = 'P' if JP_pred >= 0.1 else 'J'

    # final mbti
    mbti = ie + ns + tf + jp

    return mbti

questions = [

    "How do you usually spend your weekends?",

    "What kind of social situations make you comfortable?",

    "How do you make important decisions?",

    "Do you prefer planning things or being spontaneous?",

    "How do you react after a stressful day?",

    "Would you rather work alone or in a team?",

    "Do you enjoy practical work or theoretical ideas more?",

    "How do you prepare before important events?",

    "What type of conversations do you enjoy the most?",

    "What motivates you the most in life?"
]

responses = []

responses = []

if "step" not in st.session_state:
    st.session_state.step = 0

if "responses" not in st.session_state:
    st.session_state.responses = []

if st.session_state.step < len(questions):

    st.subheader(
        f"Question {st.session_state.step + 1}"
    )

    ans = st.text_area(
        questions[st.session_state.step]
    )

    if st.button("Next"):

        if ans.strip() != "":

            st.session_state.responses.append(ans)

            st.session_state.step += 1

            st.rerun()

else:

    full_text = " ".join(
        st.session_state.responses
    )

    result = predict_mbti(full_text)

    st.success(
        f"Predicted MBTI Type: {result}"
    )

full_text = " ".join(responses)

